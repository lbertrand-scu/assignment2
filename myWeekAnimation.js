const tooltipSocialMonday = document.getElementById("tooltipSocialMonday");

function addTooltipSocialMonday(rect, text) {
	rect.addEventListener("mouseenter", (e) => {
		tooltipSocialMonday.innerText = text;
		tooltipSocialMonday.style.opacity = 1
	});

	rect.addEventListener("mouseleave", () => {
		tooltipSocialMonday.style.opacity = 0;
	});
}

